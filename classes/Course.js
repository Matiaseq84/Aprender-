export class Course {
    constructor(id, name, price, capacity, schedule, teacher, status, enrolledStudents) {
        this.id = id,
        this.name = name,
        this.price = price,
        this.capacity = capacity,
        this.schedule = schedule,
        this.teacher = teacher,
        this.status = status,
        this.enrolledStudents = enrolledStudents
    }
}

